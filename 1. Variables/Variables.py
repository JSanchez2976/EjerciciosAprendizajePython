# Variables en Python

# Declaracion e inicializacion de variables
edad = 18
altura = 1.81
pais = 'España'

# Acceder a las variables
print("Valores iniciales: ")
print('Edad:',edad)
print("Altura:",altura)
print("Pais:",pais)

# Modificar el valor de una variable
edad = 30
altura = 1.68

# Acceder a las variables
print("Valores modificados: ")
print('Edad:',edad)
print("Altura:",altura)
print("Pais:",pais)

# En python el valor es dinámico
edad = 'treinta'
print("Edad:",edad)

# Si queremos acceder a una variable no declarada manda error
telefono ='234234999'
print("Telefono:",telefono)