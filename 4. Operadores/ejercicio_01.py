# Operadores aritmeticos

a =10
b = 3

# Suma
suma = a +b
print(f'Suma: {suma}')

# Resta
resta = a-b
print(f'Resta: {resta}')

# Multiplicacion
multiplicacion = a * b
print(f'Multiplicacion: {multiplicacion}')

# Division
division = a/b
print(f'Division: {division:.2f}')

# Division entera
division_entera = a//b
print(f'Division entera: {division_entera}')

# Modulo
modulo = a%b
print(f'Modulo de division = {modulo}')

# Potencia
exponente = a**b
print(f'Exponente: {exponente}')