# Operadores de asignacion compuestos
a, b = 10,15
print(f'Valor inicial a = {a}, b = {b}')

# Operador +=
a+=b # a = a+b
print(f'Operador a += b es: {a}')

# Operador -=
a=10
a-=b # a = a-b
print(f'Operador a -= b es: {a}')

# Operador *=
a=10
a*=b # a = a*b
print(f'Operador a *= b es: {a}')

# Operador /=
a=10
a/=b # a = a/b
print(f'Operador a /= b es: {a:.2f}')