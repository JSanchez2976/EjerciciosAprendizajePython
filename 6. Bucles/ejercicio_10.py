# Range

# 0 al 4
print("Numeros del 0 al 4: ")
for i in range (5):
    print(i, end= " ")

print("\nNumeros del 10 al 20: ")
for i in range (10,21):
    print(i,end=" ")

print("\nNumeros del 20 al 30 de 2 en 2")
for i in range (20,31,2):
    print(i,end= " ")

print("\nNumeros del 30 al 20 de 2 en 2")
for i in range (30,19,-2):
    print(i,end= " ")